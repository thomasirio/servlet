
public class Partita {
	
	private Integer idpartita;
	private String nomecampo;
	private String indirizzocampo;
	private String citta;
	private String provincia;
	private Float costo;
	private Integer nmancanti;
	private String data;
	private String ora;
	private String amministratore;
	
	public Partita(){}
	
	public Integer getIdpartita() {
		return idpartita;
	}
	public void setIdpartita(Integer idpartita) {
		this.idpartita = idpartita;
	}
	public String getNomecampo() {
		return nomecampo;
	}
	public void setNomecampo(String nomecampo) {
		this.nomecampo = nomecampo;
	}
	public String getIndirizzocampo() {
		return indirizzocampo;
	}
	public void setIndirizzocampo(String indirizzocampo) {
		this.indirizzocampo = indirizzocampo;
	}
	public String getCitta() {
		return citta;
	}
	public void setCitta(String citta) {
		this.citta = citta;
	}
	public String getProvincia() {
		return provincia;
	}
	public void setProvincia(String provincia) {
		this.provincia = provincia;
	}
	public Float getCosto() {
		return costo;
	}
	public void setCosto(Float costo) {
		this.costo = costo;
	}
	public Integer getNmancanti() {
		return nmancanti;
	}
	public void setNmancanti(Integer nmancanti) {
		this.nmancanti = nmancanti;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getAmministratore() {
		return amministratore;
	}
	public void setAmministratore(String amministratore) {
		this.amministratore = amministratore;
	}

	public String getOra() {
		return ora;
	}

	public void setOra(String ora) {
		this.ora = ora;
	}
	

}
